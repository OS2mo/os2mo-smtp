@pytest.mark.integration_test
async def test_rolebinding_sends_email(
    graphql_client: GraphQLClient,
    active_directory: UUID,
    rolle: UUID,
    create_ituser,
    create_rolebinding,
    trigger_event,
    get_sent_mails,
) -> None:
    """A new rolebinding triggers a notice to datagruppen describing the IT-user."""
    person = (
        await graphql_client._testing__create_employee(
            input=EmployeeCreateInput(given_name="Mick", surname="Jagger")
        )
    ).uuid
    ituser = await create_ituser(
        user_key="ADUSER-123", itsystem=active_directory, person=person
    )
    rolebinding = await create_rolebinding(ituser=ituser, role=rolle)

    await trigger_event("rolebinding", rolebinding)

    # All inputs are fixed, so the whole email is deterministic: assert it equals
    # the template rendered with the expected context.
    expected_body = load_template("alert_on_rolebinding.html").render(
        context={
            "person": "Mick Jagger",
            "ituser": "ADUSER-123",
            "itsystem": "Active Directory",
            "roles": [{"name": "Rolle"}],
        }
    )
    mail = one(await get_sent_mails())
    assert mail.subject == "En IT-bruger er blevet oprettet i MO"
    assert mail.recipients == ["<datagruppen@silkeborg.dk>"]
    assert mail.html.strip() == expected_body.strip()


@pytest.mark.integration_test
async def test_rolebinding_identical_email_is_deduplicated(
    graphql_client: GraphQLClient,
    active_directory: UUID,
    rolle: UUID,
    create_ituser,
    create_rolebinding,
    trigger_event,
    get_sent_mails,
) -> None:
    """Re-triggering an identical rolebinding does not send a second email."""
    person = (
        await graphql_client._testing__create_employee(
            input=EmployeeCreateInput(given_name="Mick", surname="Jagger")
        )
    ).uuid
    ituser = await create_ituser(
        user_key="ADUSER-123", itsystem=active_directory, person=person
    )
    rolebinding = await create_rolebinding(ituser=ituser, role=rolle)

    await trigger_event("rolebinding", rolebinding)
    assert len(await get_sent_mails()) == 1

    with capture_logs() as cap_logs:
        await trigger_event("rolebinding", rolebinding)

    assert "Email is identical to the previous" in str(cap_logs)
    assert len(await get_sent_mails()) == 1
