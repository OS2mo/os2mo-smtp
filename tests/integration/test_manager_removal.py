@pytest.mark.integration_test
async def test_terminated_manager_sends_email(
    graphql_client: GraphQLClient,
    create_org_unit,
    create_manager,
    trigger_event,
    get_sent_mails,
) -> None:
    """A manager terminated in the past triggers a removal notice to datagruppen."""
    person = (
        await graphql_client._testing__create_employee(
            input=EmployeeCreateInput(given_name="Keith", surname="Richards")
        )
    ).uuid
    org_unit = await create_org_unit(name="Stones", user_key="stones")
    manager = await create_manager(person=person, org_unit=org_unit)
    await graphql_client._testing__terminate_manager(
        input=ManagerTerminateInput(uuid=manager, to=PAST)
    )

    await trigger_event("manager", manager)

    # All inputs are fixed, so the whole email is deterministic: assert it equals
    # the termination template rendered with the expected context (which also
    # proves the terminated branch — not the vacant one — was taken).
    expected_body = load_template("alert_on_manager_termination.html").render(
        context={
            "name": "Keith Richards",
            "to_date": date(2020, 1, 1),
            "location": "Stones",
            "user_key": "stones",
        }
    )
    mail = one(await get_sent_mails())
    assert mail.subject == "En medarbejder er blevet fjernet fra lederfanen"
    assert mail.recipients == ["<datagruppen@silkeborg.dk>"]
    assert mail.html.strip() == expected_body.strip()


