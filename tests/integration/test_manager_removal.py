# MO normalizes termination dates to Europe/Copenhagen and requires them to fall
# at midnight there; a UTC midnight becomes 01:00 CET and is rejected.
MO_TZ = ZoneInfo("Europe/Copenhagen")
PAST = datetime(2020, 1, 1, tzinfo=MO_TZ)
FUTURE = datetime(2099, 1, 1, tzinfo=MO_TZ)
# Validity for created objects — far enough in the past that they're current.
SEED = RAValidityInput(from_=datetime(1930, 1, 1, tzinfo=timezone.utc))


@pytest.mark.integration_test
async def test_manager_not_found(trigger_event, get_sent_mails) -> None:
    """A non-existent manager UUID sends no email."""
    with capture_logs() as cap_logs:
        await trigger_event("manager", uuid4())

    assert "Manager not found" in str(cap_logs)
    assert await get_sent_mails() == []


