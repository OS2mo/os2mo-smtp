@pytest.mark.integration_test
async def test_ituser_future_termination_sends_termination_email(
    context,
    session: AsyncSession,
    graphql_client: GraphQLClient,
    email_client: MagicMock,
):
    """A termination scheduled for the future should still produce a
    termination alert, not a creation alert. The ituser's `current` state
    is populated (because `to` is in the future), but no validity is
    open-ended, so the handler must treat it as terminated."""
    ituser_uuid, _ = await _setup_ituser(graphql_client)
    midnight = datetime.now().replace(hour=0, minute=0, second=0, microsecond=0)
    future = midnight + timedelta(days=30)
    await graphql_client._testing__terminate_it_user(uuid=ituser_uuid, to=future)

    await alert_on_ituser(context, ituser_uuid, None, graphql_client, session)

    email_client.send_email.assert_called_once()
    assert (
        email_client.send_email.call_args.kwargs["subject"]
        == "En IT-bruger er blevet termineret i MO"
    )


@pytest.mark.integration_test
async def test_ituser_bounded_update_sends_creation_email(
    context,
    session: AsyncSession,
    graphql_client: GraphQLClient,
    email_client: MagicMock,
):
    """A bounded update (user_key changed for a fixed period) leaves a
    trailing open-ended validity for the resumed state, so the handler must
    not flag the ituser as terminated."""
    ituser_uuid, _ = await _setup_ituser(graphql_client)
    midnight = datetime.now().replace(hour=0, minute=0, second=0, microsecond=0)
    yesterday = midnight - timedelta(days=1)
    tomorrow = midnight + timedelta(days=1)
    await graphql_client._testing__update_it_user(
        uuid=ituser_uuid,
        user_key="TEMPORARY",
        from_=yesterday,
        to=tomorrow,
    )

    await alert_on_ituser(context, ituser_uuid, None, graphql_client, session)

    email_client.send_email.assert_called_once()
    assert (
        email_client.send_email.call_args.kwargs["subject"]
        == "En IT-bruger er blevet oprettet i MO"
    )


@pytest.mark.integration_test
async def test_ituser_open_ended_update_sends_new_email(
    context,
    session: AsyncSession,
    graphql_client: GraphQLClient,
    email_client: MagicMock,
):
    """A permanent (open-ended) user_key change produces a new alert: the
    timeline grows from one validity to two, but the latest still has
    to=None, so the ituser is not terminated and the rendered body changes."""
    ituser_uuid, _ = await _setup_ituser(graphql_client)
    midnight = datetime.now().replace(hour=0, minute=0, second=0, microsecond=0)

    await alert_on_ituser(context, ituser_uuid, None, graphql_client, session)
