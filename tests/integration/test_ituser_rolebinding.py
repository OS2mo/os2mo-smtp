@pytest.mark.integration_test
async def test_ituser_termination_content_change_sends_new_email(
    context,
    session: AsyncSession,
    graphql_client: GraphQLClient,
    email_client: MagicMock,
):
    """When a termination row exists with a stale content_hash, the next
    event re-sends the alert and updates the row. Exercises the
    'existing.content_hash = content_hash' branch on the termination path."""
    ituser_uuid, _ = await _setup_ituser(graphql_client)
    await graphql_client._testing__terminate_it_user(
        uuid=ituser_uuid, to=datetime(2015, 6, 1)
    )

    await alert_on_ituser(context, ituser_uuid, None, graphql_client, session)
    email_client.send_email.assert_called_once()

    row = await session.scalar(
        select(SentAlert).where(
            SentAlert.alert_type == "ituser_termination",
            SentAlert.object_uuid == ituser_uuid,
        )
    )
    assert row is not None
    original_hash = row.content_hash
    row.content_hash = "stale-hash-from-an-earlier-deploy"
    await session.flush()

    await alert_on_ituser(context, ituser_uuid, None, graphql_client, session)

    assert email_client.send_email.call_count == 2
    refreshed = await session.scalar(
        select(SentAlert).where(
            SentAlert.alert_type == "ituser_termination",
            SentAlert.object_uuid == ituser_uuid,
        )
    )
    assert refreshed is not None
    assert refreshed.content_hash == original_hash


